package typecastingdataypes;

public class Demo {

	public static void main(String[] args) {
		
		// implicit typecasting
		byte b = 100;
		
		short s = b;
		
		int i = s;
		
		long l = i;
		
		float f = l;
		
		double d= f;
		
		// char , boolean - special typecasting need
		
		System.out.println(f);
	}
}
