package com.day2.zzjava8Examples;

import java.util.HashSet;

public class HashSetExample {

	public static void main(String[] args) {
		
		HashSet<String> hs = new HashSet<String>();
		
		hs.add("Prakash");
		hs.add("Akash");
		hs.add("Chandrakant");
		hs.add("Pooja");
		
		System.out.println(hs);
		
	}
}
