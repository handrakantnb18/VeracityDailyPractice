package NestedLoopFourtyOneToFifty;

public class PrintSquareStar {

	public static void main(String[] args) {
		
		// 41. Print square star pattern using nested loops.

		
		
		// simple Square pattern in nested loop
		
		for (int i = 1; i <= 5; i++)
		{
			for (int j = 1; j <= 5; j++)
			{
				System.out.print("*");
			}
			System.out.println();
		}
	}
}
