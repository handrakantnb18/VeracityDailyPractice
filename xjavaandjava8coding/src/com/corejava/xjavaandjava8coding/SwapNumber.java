package com.corejava.xjavaandjava8coding;

public class SwapNumber {

	public static void main(String[] args) {
		
		int a = 15;
		
		int b = 25;
		
		a = a + b;
		b = a - b;
		a = a - b;
		
		System.out.println(a);
		
		System.out.println(b);
		
		
		int x = 45;
		
		int y = 54;
		
		x = x + y;
		y = x - y;
		x = x - y;
		
		System.out.println(x);
		
		System.out.println(y);
		
	}
}
