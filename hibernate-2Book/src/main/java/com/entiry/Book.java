package com.entiry;

public class Book {

	private int id;
	
	private String bookname;
	
	private String auther;
	
	private int price;

	public Book(int id, String bookname, String auther, int price) {
		super();
		this.id = id;
		this.bookname = bookname;
		this.auther = auther;
		this.price = price;
	}
	
	
}
